    private void showDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showDetailsActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select an appointment first.");
            return;
        }
        String appointmentId = jTable1.getValueAt(row, 0).toString();
        
        Customer.AppointmentDetails details = customer.getAppointmentDetails(appointmentId);
        
        StringBuilder sb = new StringBuilder();
        sb.append("Appointment ID: ").append(details.appointmentId).append("\n");
        sb.append("Doctor ID: ").append(details.doctorId).append("\n");
        sb.append("Date: ").append(details.date).append("\n");
        sb.append("Visit Reason: ").append(details.visitReason).append("\n");
        sb.append("Payment: RM ").append(details.paymentAmount).append("\n");
        sb.append("Doctor Comment: ").append(details.doctorComment).append("\n");
        
        JOptionPane.showMessageDialog(this, sb.toString(), "Appointment Details", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_showDetailsActionPerformed

