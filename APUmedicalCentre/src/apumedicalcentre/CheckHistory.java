    private void loadAppointments(){
        DefaultTableModel model = new DefaultTableModel(new String[]{"AppointmentID","DoctorID","Date"},0);
        jTable1.setModel(model);
        
        try(BufferedReader br = new BufferedReader(new FileReader("src/Data/appointment.txt"))){
            String line;
            boolean isFirstLine = true;
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date today = new Date();
            
            while((line = br.readLine()) != null){
                if(isFirstLine){
                    isFirstLine = false;
                    continue;
                }
                String[] parts = line.split(",");
                if(parts.length >= 8){
                    String appointmentId = parts[0].trim();
                    String custId = parts[1].trim();
                    String docId = parts[3].trim();
                    String date = parts[4].trim();
                    String visitReason = parts[6].trim();
                    String status = parts[7].trim();
                    
                    if(custId.equals(customer.getUserId()) && status.equalsIgnoreCase("COMPLETED")){
                        Date appointmentDate = sdf.parse(date);
                        if(appointmentDate.before(today)){
                            model.addRow(new Object[]{appointmentId, docId, date});
                        }
                    }
                }
            }
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading appointments: " + e.getMessage());
        }
    }
    
   

