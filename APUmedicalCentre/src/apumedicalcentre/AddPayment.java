    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        int selectedRow = jTable2.getSelectedRow();
        
        // If a record is selected
        if (selectedRow != -1) {
            String customerID = jTable2.getValueAt(selectedRow, 5).toString();

            File file = new File("src/Data/customer.txt");
            String customerName = Staff.getValueById(file, customerID, "CustomerID", "Name");

            if (customerName == null || customerName.isEmpty()) {
                customerName = "unknown";
            }
            
            // Display the name and the amount of charges on label
            jLabel16.setText(customerName);
            
            String amount = jTable2.getValueAt(selectedRow, 2).toString();
            jTextField1.setText(amount);
        }
    }//GEN-LAST:event_jTable2MouseClicked

