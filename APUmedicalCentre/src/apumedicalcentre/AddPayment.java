    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int selectedRow = jTable2.getSelectedRow();
        String paymentID = jTable2.getValueAt(selectedRow, 0).toString();
        String customerName = jLabel16.getText();
        
        // Issue a receipt
        if (customerName != null && !customerName.isEmpty() && !customerName.equals("(unknown)")) {
        issueReceipt(paymentID, customerName);
        } else {
            JOptionPane.showMessageDialog(this, "Customer Name not found. Unable to issue a receipt.");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

