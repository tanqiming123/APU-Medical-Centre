    // Editer
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Get customerID to edit
        String customerID = jLabel18.getText();
        if (customerID == null || customerID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "CustomerID not found. Please select a record from the table first.");
            return;
        }

        // Get selected column to edit
        String column = (String) jComboBox3.getSelectedItem();
        if (column == null || column.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a column to edit.");
            return;
        }

        // Get new value
        String newValue = jTextField3.getText();
        if (newValue == null || newValue.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a new value.");
            return;
        }

        // File location
        File customerFile = new File("src/Data/customer.txt");

        boolean success = staff.updateRecordById(customerFile, customerID, "CustomerID", column, newValue);

        if (success) {
            // Reload table and clear the label and the TextField
            TableManager.showTable(customerFile, jTable2);
            jLabel18.setText("");
            jTextField3.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

