    // Delete customer
    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
                                                     
        // Get CustomerID from label
        String customerID = jLabel18.getText();

        if (customerID == null || customerID.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "CustomerID not found. Please select a record from the table first.");
            return;
        }

        File customerFile = new File("src/Data/customer.txt");

        // Call Staff method to delete
        boolean deleted = staff.deleteRecordById(customerFile, customerID, "CustomerID");

        if (deleted) {
            // Reload table and clear the label
            TableManager.showTable(customerFile, jTable2);

            jLabel18.setText("");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

