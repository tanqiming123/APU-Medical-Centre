    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String paymentID = jLabel16.getText();

        if (paymentID == null || paymentID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No PaymentID has been selected.");
            return;
        }
        
        // Retrieve the CustomerID from payment.txt
        File paymentFile = new File("src/Data/payment.txt");
        String customerID = Staff.getValueById(paymentFile, paymentID, "PaymentID", "CustomerID");

        if (customerID == null || customerID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "CustomerID cannot find");
            return;
        }

        // Retrieve CustomerName from customer.txt
        File customerFile = new File("src/Data/customer.txt");
        String customerName = Staff.getValueById(customerFile, customerID, "CustomerID", "Name");

        if (customerName != null && !customerName.isEmpty()) {
        issueReceipt(paymentID, customerName);
        } else {
            JOptionPane.showMessageDialog(this, "Customer Name not found. Unable to issue a receipt.");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

