    @Override
    public void issueReceipt(String paymentID, String customerName) {
        int result = JOptionPane.showConfirmDialog(
            this,
            "PaymentID: " + paymentID + "\n" +
            "Customer Name: " + customerName + "\n\n" +
            "Do you want to issue this receipt?",
            "Confirm",
            JOptionPane.OK_CANCEL_OPTION
        );

        if (result == JOptionPane.OK_OPTION) {
            JOptionPane.showMessageDialog(this, "The receipt was issued");
        }
    }


